def philosophy_option_names() -> list[str]:
    return [p["name"] for p in PHILOSOPHY_PRESETS] + [OTHER_OPTION_LABEL]


def investor_option_names() -> list[str]:
    return [p["name"] for p in INVESTOR_PRESETS] + [OTHER_OPTION_LABEL]


def resolve_philosophy(selection: str, other_instruction: str) -> tuple[str, str]:
    """(system_prompt, 헤딩용 짧은 표시명) 반환."""
    if selection == OTHER_OPTION_LABEL:
        if not other_instruction.strip():
            raise ValueError("기타를 선택했습니다. 역할·관점 설명을 입력해 주세요.")
        lab = _short_label(other_instruction, prefix="기타")
        return _other_philosophy_prompt(other_instruction), lab
    for p in PHILOSOPHY_PRESETS:
        if p["name"] == selection:
            return p["prompt"], p["name"]
    raise ValueError(f"선택을 찾을 수 없습니다: {selection}")


def resolve_investor(selection: str, other_instruction: str) -> tuple[str, str]:
    if selection == OTHER_OPTION_LABEL:
        if not other_instruction.strip():
            raise ValueError("기타를 선택했습니다. 역할·관점 설명을 입력해 주세요.")
        lab = _short_label(other_instruction, prefix="기타")
        return _other_investor_prompt(other_instruction), lab
    for p in INVESTOR_PRESETS:
        if p["name"] == selection:
            return p["prompt"], p["name"]
    raise ValueError(f"선택을 찾을 수 없습니다: {selection}")


def _short_label(text: str, *, prefix: str, max_len: int = 42) -> str:
    t = " ".join(text.strip().split())
    if len(t) <= max_len:
        return f"{prefix}: {t}" if t else prefix
    return f"{prefix}: {t[: max_len - 1]}…"
